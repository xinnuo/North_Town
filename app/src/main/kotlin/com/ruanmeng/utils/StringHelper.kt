package com.ruanmeng.utils

fun String.getDateFormat(): String {
    val divide = toInt() / 12
    val surplus = toInt() % 12
    return if (divide <= 0) "${surplus}个月"
    else {
        when (surplus) {
            6 -> "${divide}年半"
            0 -> "${divide}年"
            else -> "${divide}年${surplus}个月"
        }
    }
}
